	public Khazad() {
		super();
		name = "Khazad";
		keySize = NUM_BYTES_IN_KEY;
		stateSize = NUM_BYTES_IN_STATE;
		numRounds = NUM_ROUNDS;
	}

	public boolean canInvertKeySchedule() {
		return true;
	}
	
	public ByteArray computeExpandedKey(ByteArray keyPart, int round) {
		int numRoundKeysInPart = 2;
		secretKey = new ByteArray((numRounds + 1) * stateSize);
 
		if (round < numRounds) {
			secretKey.copyBytes(keyPart, 0, round * stateSize);
		} else if (round == numRounds) {
			secretKey.copyBytes(keyPart, 0, (round - 1) * stateSize);
		}
		
		if (round + 2 <= numRounds) {
			secretKey = expandKeyForwards(secretKey, keyPart, round + numRoundKeysInPart);
		}
		
		if (round == numRounds) {
			secretKey = expandKeyBackwards(secretKey, keyPart, round - 2);
		} else if (round > 0) {
			secretKey = expandKeyBackwards(secretKey, keyPart, round - 1);
		}
		
		return secretKey;
	}
	
	public ByteArray decryptRounds(ByteArray block, int fromRound, int toRound) {
		state = block.clone();
		
		for (int round = toRound; round >= fromRound; round--) {
			state = addRoundKey(round, state);
			
			if (round != numRounds) {
				state = invertTheta(state);
			}
			
			state = invertGamma(state);
		}
		
		if (fromRound == 1) {
			state = addRoundKey(0, state);
		}
		
		return state;
	}
	
	public ByteArray encryptRounds(ByteArray block, int fromRound, int toRound) {
		state = block.clone();
		
		if (fromRound == 1) {
			state = addRoundKey(0, state);
		}
		
		for (int round = fromRound; round <= toRound; round++) {
			state = gamma(state);
			
			if (round != numRounds) {
				state = theta(state);
			}
			
			state = addRoundKey(round, state);
		}
		
		return state;
	}
	
	public int getNumActiveComponentsInKeySchedule() {
		return (numRounds + 1) * stateSize;
	}
	
	public ByteArray getRoundKey(int round) {
		int from = round * stateSize;
		int to = from + stateSize;
		return this.secretKey.splice(from, to);
	}

	public boolean injectsKeyAtRoundEnd(int round) {
		return true;
	}
	
	public void setKey(ByteArray key) {
		checkKeySize(key.length());
		secretKey = expandKey(key.clone());
	}

	public void setExpandedKey(ByteArray expandedKey) {
		secretKey = expandedKey.clone();
	}
	
	private ByteArray addRoundConstant(ByteArray roundKey, int round) {
		int[] roundConstants = getRoundConstants(round);
		
		for (int i = 0; i < stateSize; i++) {
			roundKey.set(i, roundKey.get(i) ^ roundConstants[i]);
		}
		
		return roundKey;
	}
	
	private void checkKeySize(int length) {
		if (length != keySize) {
			throw new InvalidKeySizeError(length);
		}
	}
	
	private ByteArray expandKey(ByteArray key) {
		int numRoundKeys = numRounds + 1;
		secretKey = new ByteArray(numRoundKeys * stateSize);
		secretKey = expandKeyForwards(secretKey, key, 0);
		return secretKey;
	}
	
	private ByteArray expandKeyBackwards(ByteArray secretKey, ByteArray key, int round) {
		ByteArray nextKey = key.splice(0, stateSize); // K[i-2] = K_(0 .. 7)
		ByteArray nextNextKey = key.splice(stateSize, 2 * stateSize); // K[i-1] = K_(8 .. 15)
		ByteArray roundKey;
		
		for (; round >= 0; round--) {
			roundKey = nextKey.clone();
			roundKey = gamma(roundKey);
			roundKey = theta(roundKey);
			roundKey = addRoundConstant(roundKey, round + 2);
			roundKey.xor(nextNextKey);
			
			secretKey.copyBytes(roundKey, 0, round * stateSize);
			nextNextKey = nextKey;
			nextKey = roundKey;
		}
		
		return secretKey;
	}
	
	private ByteArray expandKeyForwards(ByteArray secretKey, ByteArray key, int round) {
		ByteArray prevPrevKey = key.splice(0, stateSize); // K[i-2] = K_(0 .. 7)
		ByteArray prevKey = key.splice(stateSize, 2 * stateSize); // K[i-1] = K_(8 .. 15)
		ByteArray roundKey;
		
		for (; round <= numRounds; round++) {
			roundKey = prevKey.clone();
			roundKey = gamma(roundKey);
			roundKey = theta(roundKey);
			roundKey = addRoundConstant(roundKey, round);
			roundKey.xor(prevPrevKey);
			
			secretKey.copyBytes(roundKey, 0, round * stateSize);
			prevPrevKey = prevKey;
			prevKey = roundKey;
		}
		
		return secretKey;
	}
	
	private ByteArray gamma(ByteArray state) {
		for (int index = 0; index < stateSize; index++) {
			state.set(index, SBOX[state.get(index)]);
		}
		
		return state;
	}

	private int[] getRoundConstants(int round) {
		return ROUND_CONSTANTS[round];
	}
	
	private ByteArray invertGamma(ByteArray state) {
		for (int index = 0; index < stateSize; index++) {
			state.set(index, INVERSE_SBOX[state.get(index)]);
		}
		
		return state;
	}
	
	private ByteArray invertTheta(ByteArray state) {
		// theta is its own inverse
		return theta(state);
	}
	
